-- ============================================================
-- Cherry Platform - Schema-separated PostgreSQL 16 DDL
-- Policy:
-- - singular table names
-- - schema separation by domain
-- - tracked_entity is a global representative-entity dictionary
-- - entity_category manages page-scoped mid-level categories
-- - tracked_entity_placement maps entity to page/category canonically
-- - page/category classification is stored per article in user_article_ai_state
-- - side_category is attached directly to user_article_state
-- - user_scoring_preference + user_digest_preference => preference
-- - allow recreating the same url_handle after soft delete in source
-- - child tables that store user_id must use composite FK so they can
--   reference only rows that belong to the same user
-- - all UUID columns in this schema are intended to store UUID v7 values
--   generated by the application layer
-- - user_article_ai_state stores entity snapshot fields copied at the time
--   of article AI processing for historical preservation
-- - agent writes raw AI payload to user_article_ai_state.agent_json_raw;
--   backend workers parse and project it into query-optimized columns
--
-- Operating rules:
-- - content.user_article_state.representative_entity_id is the final
--   confirmed representative entity used by product/backend logic
-- - content.user_article_ai_state.representative_* fields are AI draft
--   snapshot values captured at processing time
-- - one tracked_entity can belong to multiple pages
-- - but for each single page, one tracked_entity can have only one active
--   canonical category placement
-- - tracked_entity_placement replacement must use revoked_at
--   when changing a page-category mapping:
--   1) mark old row revoked_at = CURRENT_TIMESTAMP
--   2) optionally also set is_active = FALSE
--   3) insert the new canonical row
-- - do not rely on is_active = FALSE alone for replacement, because the
--   unique index is based on revoked_at IS NULL
-- - public concept documents are stored in content.concept_page and
--   content.concept_changelog, not in a separate concept schema
-- ============================================================

