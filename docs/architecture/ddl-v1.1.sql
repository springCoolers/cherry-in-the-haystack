CREATE OR REPLACE FUNCTION core.set_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
RETURN NEW;
END;
$$;

-- This trigger validates that AI snapshot fields are not arbitrary text.
-- representative_entity_name must match content.tracked_entity.name
-- representative_entity_category_name must match content.entity_category.name
-- representative_entity_id + page + category_id must match an active,
-- non-revoked canonical placement in content.tracked_entity_placement
CREATE OR REPLACE FUNCTION content.validate_user_article_ai_state_representative_name()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
v_entity_name VARCHAR(200);
    v_category_name VARCHAR(100);
BEGIN
    IF NEW.representative_entity_id IS NULL THEN
        RETURN NEW;
END IF;

SELECT name
INTO v_entity_name
FROM content.tracked_entity
WHERE id = NEW.representative_entity_id;

IF NOT FOUND THEN
        RAISE EXCEPTION
            'representative_entity_id % does not exist in content.tracked_entity',
            NEW.representative_entity_id;
END IF;

    IF NEW.representative_entity_name IS DISTINCT FROM v_entity_name THEN
        RAISE EXCEPTION
            'representative_entity_name mismatch: expected %, got %',
            v_entity_name, NEW.representative_entity_name;
END IF;

SELECT name
INTO v_category_name
FROM content.entity_category
WHERE id = NEW.representative_entity_category_id;

IF NOT FOUND THEN
        RAISE EXCEPTION
            'representative_entity_category_id % does not exist in content.entity_category',
            NEW.representative_entity_category_id;
END IF;

    IF NEW.representative_entity_category_name IS DISTINCT FROM v_category_name THEN
        RAISE EXCEPTION
            'representative_entity_category_name mismatch: expected %, got %',
            v_category_name, NEW.representative_entity_category_name;
END IF;

    PERFORM 1
    FROM content.tracked_entity_placement
    WHERE tracked_entity_id = NEW.representative_entity_id
      AND entity_page = NEW.representative_entity_page
      AND entity_category_id = NEW.representative_entity_category_id
      AND revoked_at IS NULL
      AND is_active = TRUE;

    IF NOT FOUND THEN
        RAISE EXCEPTION
            'representative placement mismatch: entity_id %, page %, category_id % is not active canonical placement',
            NEW.representative_entity_id,
            NEW.representative_entity_page,
            NEW.representative_entity_category_id;
END IF;

RETURN NEW;
END;
$$;

